print('Digite um fatorial para ser calculado')
numero  = int(input('Digite o número: '))
fatorial = int (1)
contador = int (1)

while (numero >= contador):
    fatorial = fatorial * contador
    contador = contador + 1

print('O fatorial de' , numero , 'é:', fatorial)

